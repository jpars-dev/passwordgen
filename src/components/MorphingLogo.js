function easeInOutCubic(t) {
  return t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2;
}

const morphController = {
  listeners: new Set(),
  currentIndex: 0,
  running: false,
  rafId: 0,
  timeoutId: null,

  start() {
    if (this.running) return;
    this.running = true;

    const morphDuration = 400;
    const holdDuration = 400;

    const pickNext = (current) => {
      const options = [0, 1, 2, 3, 4].filter((i) => i !== current);
      return options[Math.floor(Math.random() * options.length)];
    };

    const broadcast = (d) => {
      this.listeners.forEach((fn) => fn(d));
    };

    broadcast(LOGO_PATHS[this.currentIndex]);

    const animateTransition = () => {
      if (!this.running) return;
      const nextIndex = pickNext(this.currentIndex);
      const interp = interpolate(
        LOGO_PATHS[this.currentIndex],
        LOGO_PATHS[nextIndex],
        { maxSegmentLength: 8 }
      );
      const startTime = performance.now();

      const step = (now) => {
        if (!this.running) return;
        const elapsed = now - startTime;
        const rawT = Math.min(elapsed / morphDuration, 1);
        const t = easeInOutCubic(rawT);
        broadcast(interp(t));

        if (rawT < 1) {
          this.rafId = requestAnimationFrame(step);
        } else {
          this.currentIndex = nextIndex;
          this.timeoutId = setTimeout(animateTransition, holdDuration);
        }
      };
      this.rafId = requestAnimationFrame(step);
    };

    this.timeoutId = setTimeout(animateTransition, holdDuration);
  },

  stop() {
    this.running = false;
    if (this.timeoutId) clearTimeout(this.timeoutId);
    cancelAnimationFrame(this.rafId);
  },

  subscribe(fn) {
    this.listeners.add(fn);
    if (this.listeners.size === 1) this.start();
    fn(LOGO_PATHS[this.currentIndex]);
    return () => {
      this.listeners.delete(fn);
      if (this.listeners.size === 0) this.stop();
    };
  },
};

export default function MorphingLogo({
  size = 40,
  circleColor = '#5a0a1a',
  shapeColor = '#FFEFC8',
}) {
  const pathRef = useRef(null);

  useEffect(() => {
    const el = pathRef.current;
    if (!el) return;

    return morphController.subscribe((d) => {
      el.setAttribute('d', d);
    });
  }, []);

  return (
    <svg
      viewBox="0 0 1000 1000"
      style={{ width: size, height: size, flexShrink: 0 }}
    >
      <circle cx="500" cy="500" r="434.03" fill={circleColor} />
      <path ref={pathRef} fill={shapeColor} d="" />
    </svg>
  );
}
